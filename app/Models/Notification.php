    /**
     * Get the notification color based on type
     */
    public function getColorAttribute(): string
    {
        return match($this->type) {
            'info' => 'blue',
            'success' => 'green',
            'warning' => 'yellow',
            'error' => 'red',
            default => 'gray'
        };
    }
}